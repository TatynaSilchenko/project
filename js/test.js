function fib(count) {
    let fibStr = '';
    let first = 0;
    let second = 1;

    if (!Number.isInteger(count) || isNaN(count) || typeof (count) !== 'number' || count < 0) {
        return "";
    }

    for (let i = 0; i < count; i++) {

        switch (i) {
            case 0: {
                fibStr = first + ' ';
                break;
            }
            case 1: {
                fibStr += second + ' ';
                break;
            }
            default: {
                let sum = first + second;
                fibStr += sum + ' ';
                first = second;
                second = sum;
                break;
            }

        }
    }
    return fibStr.trim();
}
